#[test]
fn ed25519_rfc8032_vectors() {
    let doc = load("ed25519-rfc8032.json");
    for case in cases(&doc) {
        let msg = hexf(case, "bytes");
        let pk = ed25519::PublicKey::from_bytes(arr(&hexf(case, "public_key")));
        let sig = ed25519::Signature::from_bytes(arr(&hexf(case, "signature")));
        if is_positive(case) {
            let kp = ed25519::Keypair::from_seed(arr(&hexf(case, "secret_key")));
            assert_eq!(kp.public_key(), pk, "{} pk derivation", case["name"]);
            assert_eq!(kp.sign_raw(&msg), sig, "{} sign", case["name"]);
            verify_raw(&pk, &msg, &sig).unwrap_or_else(|e| panic!("{}: {e}", case["name"]));
        } else {
            assert!(verify_raw(&pk, &msg, &sig).is_err(), "{}", case["name"]);
        }
    }
}

