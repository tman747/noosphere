/// Legacy Ascent-era artifacts must fail NOOSPHERE verification
/// (cross-domain rejection law, plan section 3.3).
#[test]
fn cross_domain_rejection_vectors() {
    let doc = load("cross-domain.json");
    for case in cases(&doc) {
        let name = case["name"].as_str().expect("name");
        assert!(
            !is_positive(case),
            "{name}: cross-domain cases are negative"
        );
        let msg = hexf(case, "bytes");

        if case.get("old_hash32").is_some() {
            // Reproduce the legacy keyed construction from its hex-encoded
            // identity strings, prove we faithfully rebuilt it, then prove
            // the NOOS domain hash differs.
            let old_context = String::from_utf8(hexf(case, "old_context_hex")).unwrap();
            let old_key_material = hexf(case, "old_key_material_hex");
            let old_key = blake3::derive_key(&old_context, &old_key_material);
            let mut hasher = blake3::Hasher::new_keyed(&old_key);
            hasher.update(&msg);
            let rebuilt_old = *hasher.finalize().as_bytes();
            assert_eq!(
                rebuilt_old.as_slice(),
                hexf(case, "old_hash32"),
                "{name}: legacy construction mismatch"
            );
            let noos = hash_domain(domain(case), &[&msg]).unwrap();
            assert_eq!(
                noos.as_bytes().as_slice(),
                hexf(case, "noos_hash32"),
                "{name}: NOOS digest mismatch"
            );
            assert_ne!(noos.as_bytes().as_slice(), rebuilt_old.as_slice(), "{name}");
            continue;
        }

        let signature = hexf(case, "signature");
        match signature.len() {
            96 => {
                let pk = BlsPublicKey::from_bytes(arr(&hexf(case, "public_key")));
                let sig = BlsSignature::from_bytes(arr(&signature));
                // Must fail under the named domain AND under every
                // registered BLS DST.
                for dom in DomainId::ALL {
                    if dom.kind() != DomainKind::BlsDst {
                        continue;
                    }
                    assert!(
                        bls_verify(dom, &pk, &msg, &sig).is_err(),
                        "{name}: verified under {}",
                        dom.registry_id()
                    );
                }
                // Sanity: the artifact does verify under its legacy DST,
                // proving the failure is domain separation, not corruption.
                let old_dst = hexf(case, "old_dst_hex");
                verify_with_dst(&pk, &msg, &sig, &old_dst)
                    .unwrap_or_else(|e| panic!("{name}: legacy artifact malformed: {e}"));
            }
            64 => {
                let pk = ed25519::PublicKey::from_bytes(arr(&hexf(case, "public_key")));
                let sig = ed25519::Signature::from_bytes(arr(&signature));
                for dom in DomainId::ALL {
                    if dom.kind() != DomainKind::Ed25519Prefix {
                        continue;
                    }
                    assert!(
                        verify_domain(dom, &pk, &[&msg], &sig).is_err(),
                        "{name}: verified under {}",
                        dom.registry_id()
                    );
                }
                // Sanity: valid under its legacy construction.
                let legacy_msg = match case.get("old_prefix_hex") {
                    Some(_) => {
                        let mut m = hexf(case, "old_prefix_hex");
                        m.extend_from_slice(&msg);
                        m
                    }
                    None => msg.clone(),
                };
                verify_raw(&pk, &legacy_msg, &sig)
                    .unwrap_or_else(|e| panic!("{name}: legacy artifact malformed: {e}"));
            }
            other => panic!("{name}: unexpected signature length {other}"),
        }
    }
}
