//! Conformance tests over `protocol/vectors/crypto/*.json`.
//!
//! Sources (documented per file in each JSON `description`):
//! * `blake3-official.json` — official BLAKE3 repository test vectors;
//! * `ed25519-rfc8032.json` — RFC 8032 section 7.1;
//! * `hkdf-rfc5869.json` — RFC 5869 appendix A (SHA-256 cases);
//! * `bls12381-ietf.json` — ethereum/bls12-381-tests v0.1.2 for the IETF
//!   ciphersuite `BLS_SIG_BLS12381G2_XMD:SHA-256_SSWU_RO_POP_`;
//! * `domain-hash.json`, `keyed-domain-hash.json`,
//!   `ed25519-noos-domains.json`, `bls-noos-dst.json`,
//!   `hkdf-noos-domains.json`, `feldman-threshold.json` — NOOS-domain KATs
//!   generated by independent implementations (Python blake3 / cryptography
//!   / py_ecc / pure-Python RFC 5869);
//! * `cross-domain.json` — legacy Ascent-era artifacts that must FAIL
//!   NOOSPHERE verification (old identity strings appear hex-encoded only).

#![allow(clippy::unwrap_used, clippy::expect_used, clippy::panic)]

use crate::bls::{aggregate_verify_with_dst, fast_aggregate_verify_with_dst, verify_with_dst};
use crate::ed25519::verify_raw;
use crate::hkdf::hkdf_sha256_raw;
use crate::*;
use serde_json::Value;
use std::path::PathBuf;

/// IETF ciphersuite DST used by the imported standard vectors (test-only;
/// protocol call sites use registered `BLS_DST` rows).
const IETF_POP_DST: &[u8] = b"BLS_SIG_BLS12381G2_XMD:SHA-256_SSWU_RO_POP_";

fn vectors_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("..")
        .join("..")
        .join("protocol")
        .join("vectors")
        .join("crypto")
}

fn load(name: &str) -> Value {
    let path = vectors_dir().join(name);
    let text = std::fs::read_to_string(&path)
        .unwrap_or_else(|e| panic!("cannot read {}: {e}", path.display()));
    serde_json::from_str(&text).unwrap_or_else(|e| panic!("bad JSON in {name}: {e}"))
}

fn cases(doc: &Value) -> &Vec<Value> {
    doc["cases"].as_array().expect("cases array")
}

fn hexf(case: &Value, field: &str) -> Vec<u8> {
    hex::decode(
        case[field]
            .as_str()
            .unwrap_or_else(|| panic!("missing {field}: {case}")),
    )
    .unwrap_or_else(|e| panic!("bad hex in {field}: {e}"))
}

fn arr<const N: usize>(bytes: &[u8]) -> [u8; N] {
    <[u8; N]>::try_from(bytes).unwrap_or_else(|_| panic!("expected {N} bytes, got {}", bytes.len()))
}

fn domain(case: &Value) -> DomainId {
    let id = case["domain_id"].as_str().expect("domain_id");
    DomainId::from_registry_id(id).unwrap_or_else(|| panic!("unknown domain_id {id}"))
}

fn is_positive(case: &Value) -> bool {
    match case["kind"].as_str().expect("kind") {
        "positive" => true,
        "negative" => false,
        other => panic!("unknown kind {other}"),
    }
}

#[test]
fn blake3_official_vectors() {
    let doc = load("blake3-official.json");
    let key: [u8; 32] = arr(doc["key_ascii"].as_str().expect("key_ascii").as_bytes());
    for case in cases(&doc) {
        let input = hexf(case, "bytes");
        let expected = hexf(case, "hash32");
        let mut hasher = blake3::Hasher::new();
        hasher.update(&input);
        let got = *hasher.finalize().as_bytes();
        if is_positive(case) {
            assert_eq!(got.as_slice(), expected, "{}", case["name"]);
            let keyed_expected = hexf(case, "keyed_hash32");
            let mut keyed = blake3::Hasher::new_keyed(&key);
            keyed.update(&input);
            assert_eq!(
                keyed.finalize().as_bytes().as_slice(),
                keyed_expected,
                "{} keyed",
                case["name"]
            );
        } else {
            assert_ne!(got.as_slice(), expected, "{}", case["name"]);
        }
    }
}

