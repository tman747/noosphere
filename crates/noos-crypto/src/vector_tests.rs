#[test]
fn bls12381_ietf_vectors() {
    let doc = load("bls12381-ietf.json");
    for case in cases(&doc) {
        let name = case["name"].as_str().expect("name");
        let positive = is_positive(case);
        match case["op"].as_str().expect("op") {
            "sign" => {
                let privkey = hexf(case, "privkey");
                let msg = hexf(case, "bytes");
                match BlsSecretKey::from_bytes(arr(&privkey)) {
                    Ok(sk) => {
                        assert!(positive, "{name}: invalid key accepted");
                        let expected = BlsSignature::from_bytes(arr(&hexf(case, "signature")));
                        let got = sk.sign_with_dst(&msg, IETF_POP_DST);
                        assert_eq!(got, expected, "{name}");
                        verify_with_dst(&sk.public_key(), &msg, &got, IETF_POP_DST)
                            .unwrap_or_else(|e| panic!("{name}: {e}"));
                    }
                    Err(_) => assert!(!positive, "{name}: valid key rejected"),
                }
            }
            "verify" => {
                let pk = BlsPublicKey::from_bytes(arr(&hexf(case, "pubkey")));
                let sig = BlsSignature::from_bytes(arr(&hexf(case, "signature")));
                let msg = hexf(case, "bytes");
                let result = verify_with_dst(&pk, &msg, &sig, IETF_POP_DST);
                assert_eq!(result.is_ok(), positive, "{name}: {result:?}");
            }
            "aggregate" => {
                let sigs: Vec<BlsSignature> = case["signatures"]
                    .as_array()
                    .expect("signatures")
                    .iter()
                    .map(|s| {
                        BlsSignature::from_bytes(arr(
                            &hex::decode(s.as_str().expect("sig hex")).expect("hex")
                        ))
                    })
                    .collect();
                match bls_aggregate(&sigs) {
                    Ok(agg) => {
                        assert!(positive, "{name}: aggregate accepted");
                        let expected = BlsSignature::from_bytes(arr(&hexf(case, "aggregate")));
                        assert_eq!(agg, expected, "{name}");
                    }
                    Err(_) => assert!(!positive, "{name}: aggregate rejected"),
                }
            }
            "aggregate_verify" => {
                let pks: Vec<BlsPublicKey> = case["pubkeys"]
                    .as_array()
                    .expect("pubkeys")
                    .iter()
                    .map(|p| {
                        BlsPublicKey::from_bytes(arr(
                            &hex::decode(p.as_str().expect("pk hex")).expect("hex")
                        ))
                    })
                    .collect();
                let msgs: Vec<Vec<u8>> = case["messages"]
                    .as_array()
                    .expect("messages")
                    .iter()
                    .map(|m| hex::decode(m.as_str().expect("msg hex")).expect("hex"))
                    .collect();
                let msg_refs: Vec<&[u8]> = msgs.iter().map(Vec::as_slice).collect();
                let sig_bytes = hexf(case, "signature");
                if sig_bytes.len() != 96 {
                    // Truncated wire signature: rejected before parsing.
                    assert!(!positive, "{name}: truncated signature marked positive");
                    continue;
                }
                let sig = BlsSignature::from_bytes(arr(&sig_bytes));
                let result = aggregate_verify_with_dst(&pks, &msg_refs, &sig, IETF_POP_DST);
                assert_eq!(result.is_ok(), positive, "{name}: {result:?}");
            }
            "fast_aggregate_verify" => {
                let pks: Vec<BlsPublicKey> = case["pubkeys"]
                    .as_array()
                    .expect("pubkeys")
                    .iter()
                    .map(|p| {
                        BlsPublicKey::from_bytes(arr(
                            &hex::decode(p.as_str().expect("pk hex")).expect("hex")
                        ))
                    })
                    .collect();
                let msg = hexf(case, "bytes");
                let sig = BlsSignature::from_bytes(arr(&hexf(case, "signature")));
                let result = fast_aggregate_verify_with_dst(&pks, &msg, &sig, IETF_POP_DST);
                assert_eq!(result.is_ok(), positive, "{name}: {result:?}");
            }
            "deserialize_pubkey" => {
                let bytes = hexf(case, "bytes");
                let ok =
                    bytes.len() == 48 && BlsPublicKey::from_bytes(arr(&bytes)).validate().is_ok();
                assert_eq!(ok, positive, "{name}");
            }
            "deserialize_signature" => {
                let bytes = hexf(case, "bytes");
                let ok =
                    bytes.len() == 96 && BlsSignature::from_bytes(arr(&bytes)).validate().is_ok();
                assert_eq!(ok, positive, "{name}");
            }
            other => panic!("unknown op {other}"),
        }
    }
}

